#include "libft.h"

int	ft_isalnum(int chara)
{
	if ((chara >= 'a' && chara <= 'z')
		|| (chara >= 'A' && chara <= 'Z')
		|| (chara >= '0' && chara <= '9'))
		return (1);
	else
		return (0);
}
